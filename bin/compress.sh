#!/usr/bin/env bash
# bin/compress.sh — collapse a Claude Code session JSONL into a token-thrifty digest.
#
# Use cases:
#   • Long session: feed the digest back into a follow-up session for context.
#   • Audit: human-readable summary of what an agent / shell did.
#
# Usage:
#   bash bin/compress.sh <jsonl-file>            # to stdout
#   bash bin/compress.sh <jsonl-file> > out.md   # to file
#   tail -200 ~/.claude/logs/agents.jsonl | bash bin/compress.sh -
#
# Output: markdown with sections for Agents (ranked, with duration), Bash (counts), Files (top edits).
# Performance: O(n) over the input, single pass.

set -euo pipefail

INPUT="${1:-}"
if [ -z "$INPUT" ]; then
  cat <<'USAGE' >&2
usage: bash bin/compress.sh <jsonl-file | ->
  jsonl-file: path to agents.jsonl / bash.jsonl / merged stream
  -          : read from stdin
USAGE
  exit 2
fi

if ! command -v jq >/dev/null 2>&1; then
  echo "✗ jq required" >&2
  exit 1
fi

# Read into a tempfile so we can scan twice.
TMP=$(mktemp)
trap 'rm -f "$TMP"' EXIT

if [ "$INPUT" = "-" ]; then
  cat > "$TMP"
else
  [ -f "$INPUT" ] || { echo "✗ not found: $INPUT" >&2; exit 1; }
  cp "$INPUT" "$TMP"
fi

TOTAL=$(wc -l < "$TMP" | tr -d ' ')
[ "$TOTAL" -eq 0 ] && { echo "(empty input)"; exit 0; }

# ── Header ───────────────────────────────────────────────────────────────────
{
  echo "# Session digest"
  echo ""
  FIRST_TS=$(head -1 "$TMP" | jq -r '.ts // empty' 2>/dev/null)
  LAST_TS=$(tail -1 "$TMP" | jq -r '.ts // empty' 2>/dev/null)
  echo "- Lines: $TOTAL"
  [ -n "$FIRST_TS" ] && echo "- From: $FIRST_TS"
  [ -n "$LAST_TS" ] && echo "- To:   $LAST_TS"
  echo ""
} >&1

# ── Agents (ranked, with avg duration) ───────────────────────────────────────
echo "## Agents (ranked by invocations)"
echo ""
jq -rs '
  map(select(.event == "post" and .agent != null and .agent != "Bash" and .agent != "unknown")) as $rows
  | $rows
  | group_by(.agent)
  | map({
      agent: .[0].agent,
      count: length,
      avg_duration: ((map(.duration // 0) | add) / length | floor)
    })
  | sort_by(-.count)
  | (
      ["| Agent | Invocations | Avg duration (s) |", "|---|---|---|"] +
      map("| `\(.agent)` | \(.count) | \(.avg_duration) |")
    )
  | .[]
' "$TMP" 2>/dev/null || echo "(no agent events)"

echo ""

# ── Bash (top commands, frequency) ───────────────────────────────────────────
echo "## Bash commands (top 10 by frequency)"
echo ""
jq -rs '
  map(select(.tool == "Bash" and .event == "post" and .cmd != null)) as $rows
  | $rows
  | map(.cmd | split(" ")[0:3] | join(" "))
  | group_by(.)
  | map({prefix: .[0], n: length})
  | sort_by(-.n)
  | .[0:10]
  | (
      ["| Command prefix | Count |", "|---|---|"] +
      map("| `\(.prefix)` | \(.n) |")
    )
  | .[]
' "$TMP" 2>/dev/null || echo "(no bash events)"

echo ""

# ── Footer ───────────────────────────────────────────────────────────────────
echo "---"
echo ""
echo "Generated by \`bin/compress.sh\` ($(date -u +'%Y-%m-%dT%H:%M:%SZ'))."
